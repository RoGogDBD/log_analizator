#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Анализатор логов - основной модуль запуска приложения

Этот модуль предоставляет точку входа в приложение для анализа логов
банкоматного оборудования. Приложение позволяет загружать лог-файлы,
анализировать их содержимое и отображать результаты в удобном 
для пользователя виде с построчным анализом событий.

Приложение использует PyQt5 для построения графического интерфейса.
"""

import sys
from PyQt5.QtWidgets import QApplication
from ui.main_window import MainWindow

def main():
    """
    Основная функция запуска приложения.
    
    Инициализирует экземпляр приложения QApplication, создает 
    и отображает главное окно приложения, затем запускает цикл 
    обработки событий. После завершения работы приложения выполняет 
    корректное завершение работы программы.
    
    Возвращает:
        int: Код завершения программы (через sys.exit)
    """
    app = QApplication(sys.argv)
    app.setApplicationName("Анализатор логов")
    
    window = MainWindow()
    window.show()
    
    sys.exit(app.exec_())

if __name__ == "__main__":
    main()